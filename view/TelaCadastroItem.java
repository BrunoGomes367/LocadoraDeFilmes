    private void jButtonSalvarLocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarLocacaoActionPerformed
        String codFilmeStr = jFormattedTextFieldCodigoFilme.getText();
        String codClienteStr = jFormattedTextFieldCodigoCliente.getText();
        String preco1 = jFormattedTextFieldPreco.getText();
        String tipo = jComboBoxTipo.getSelectedItem().toString();
        String dataLocacao = jFormattedTextFieldDataLocacao.getText();

        if (codFilmeStr.isEmpty() || codClienteStr.isEmpty() || preco1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos corretamente."); //adicionando uma caixa de diálogo caso os campos não estejam preenchidos corretamente
            return;
        }
        int codFilme = Integer.parseInt(codFilmeStr);
        int codCliente = Integer.parseInt(codClienteStr);
        preco1 = preco1.replace(",", "."); // substitui a vírgula pelo ponto
        double preco = Double.parseDouble(preco1);
        try {
            ItemController c = new ItemController();
            FilmeController c1 = new FilmeController();
            Filme filme = c1.obterFilmePorCodigo(codFilme);
            ClienteController c2 = new ClienteController();
            Cliente cliente = c2.obterCPFPorCodigo(codCliente);
            if (filme != null && cliente != null) {
                boolean deubom = c.cadastrarItem(codFilme, codCliente, preco, tipo, dataLocacao);
                if (deubom == true && jComboBoxTipo.getSelectedIndex() != 0) {
                    JOptionPane.showMessageDialog(null, "O cadastro deu certo."); // adicionando uma caixa de diálogo caso o meu controller pegue as informações necessárias
                    this.jButtonLimpar1ActionPerformed(evt); // puxando a minha função de limpar os campos automaticamente assim que o salvamento der certo
                } else {
                    JOptionPane.showMessageDialog(null, "Preencha os campos corretamente."); //adicionando uma caixa de diálogo caso os campos não estejam preenchidos corretamente
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione um código de filme e/ou de cliente válido.");
            }            
        } catch (Exception e) {
            System.out.println("Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonSalvarLocacaoActionPerformed


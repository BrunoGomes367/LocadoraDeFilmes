    private void jButtonOKFilmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKFilmeActionPerformed
        String codigo1 = jFormattedTextFieldCodigoFilme.getText();
        if(codigo1 != null && codigo1.length() > 0){
        try {
            Integer codigo = Integer.parseInt(jFormattedTextFieldCodigoFilme.getText());
            FilmeController c = new FilmeController();
            Filme filme = c.obterFilmePorCodigo(codigo);
            
            if(filme != null){
            String titulo = filme.getTitulo();
            jTextFieldTituloFilme.setText(titulo);
            }
            else{
                JOptionPane.showMessageDialog(null, "Selecione um código de filme válido.");
            }
        } catch (NumberFormatException e) {
            System.out.println("Erro ao converter código: " + e.getMessage());
        }
        }
        else{
                JOptionPane.showMessageDialog(null, "Selecione um código de filme válido.");
        }
    }//GEN-LAST:event_jButtonOKFilmeActionPerformed
    
    
    
